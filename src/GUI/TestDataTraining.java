     private void updateBox(){
         labelColumn.setText(D.getColumnAt(i)+" :");
         comboElement.removeAllItems();
         ArrayList<String> l= new ArrayList();
         for (Object o: D.getMainSets()){
             MainSet m = (MainSet)o;
             Groups G =m.getGroupAt(i);
             for (Object E: G.getElements()){
                 if (!l.contains(((Element)E).getName())) l.add(((Element)E).getName());
             }
         }
         for (String s_: l){
             comboElement.addItem(s_);
         }
         l.clear();
     }
