    private void butNextActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_butNextActionPerformed

        if (i<D.getColumnSize()-2){

            s.add((String)comboElement.getModel().getSelectedItem());
            txt.setText(txt.getText()+"\n"+labelColumn.getText()+comboElement.getModel().getSelectedItem());
            i++;
            updateBox();
        }
        else if (butNext.getText().equals("Finish")){
            
            String result="";
            double max=0.0,cur=1.0;
            int i;
            for (Object O: D.getMainSets()){
                i=0;
                cur=1.0;
                MainSet M = (MainSet) O;
                for (Object G : M.getGroups()){
                    Groups g= (Groups) G;
                    for (Object E: g.getElements()){
                        Element e = (Element) E;
                        if(e.getName().equals(s.get(i))) cur*=((double)e.getJumlah()/D.getTotal(e.getName(), g.getName()));
                    }
                    i++;
                }
                cur*=(double)M.getTotal()/D.getTotalMain();
                
                if (cur>max){
                    max=cur;
                    result=M.getName();
                }
                
            }
            int val =JOptionPane.showConfirmDialog(this, "Hasilnya: "+result+"\nApakah anda ingin menyimpannya? Tekan Yes jika jawabannya benar"
                + "\nTekan No jika Jawabannya Salah\nTekan Cancel jika anda tidak ingin menyimpannya");
            if (val==JOptionPane.OK_OPTION || val == JOptionPane.NO_OPTION) {
                Date d = new Date();
                DateFormat dateFormat = new SimpleDateFormat("yyyy/MM/dd HH:mm:ss");
                History h = new History();
                h.read();
                h.addHistory(new String[] {dateFormat.format(d), path, (val==JOptionPane.OK_OPTION)+""});
                h.write();
            }
            dispose();
        }
        else {
            butNext.setText("Finish");
            s.add((String)comboElement.getModel().getSelectedItem());
            txt.setText(txt.getText()+"\n"+labelColumn.getText()+comboElement.getModel().getSelectedItem());
            i++;
            updateBox();
        }
        
    }//GEN-LAST:event_butNextActionPerformed

